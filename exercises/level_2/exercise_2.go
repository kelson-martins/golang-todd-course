/*
Using the following operators, write expressions and assign their values to variables:
==
<=
>=
!=
<
>
Now print each of the variables.

*/

package main

import "fmt"

func main() {
	a := 2 == 2
	b := 2 <= 2
	c := 2 >= 2
	d := 2 != 2
	e := 2 < 2
	f := 2 > 2

	fmt.Println(a)
	fmt.Println(b)
	fmt.Println(c)
	fmt.Println(d)
	fmt.Println(e)
	fmt.Println(f)
}
